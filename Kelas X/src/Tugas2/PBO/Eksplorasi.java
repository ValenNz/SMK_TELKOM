package Tugas2.PBO;

public class Eksplorasi {
    public static void main(String[] args) {
        /* Deklarasi variabel */
        float bil_a, bil_b, hasil;

        /* membuat variabel bernilai float */
        bil_a = 454.55f;
        bil_b = 546.55f;

        /* operasi penjumlahan */
        hasil = bil_a + bil_b;

        /* output dari operasi */
        System.out.println("Hasil penjumlahann kedua bilangan :");
        System.out.println(hasil);
    }
}
